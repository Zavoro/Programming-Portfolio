//MATHEMATICAL OPERATION
//use int or decimal

//PRESENTATION OR TEXT MANIPULATION
//use string or char

//collecting data from a user such as a phone number or postal code
//this data may consist of numeric characters BUT mathematical calculations are rarely performed using this data
//therefore should prefer using a string

//DATA TYPE USE CASES
//string for words, phrases, or any alphanumeric data for presentation, not calculation
//char for a single alphanumeric character
//int for a whole number
//decimal for a number with a fractional component
//bool for a true/false value


